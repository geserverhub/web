export function pageDescFor(lang, pageId) {
  const row = ERP_PAGE_DESC[pageId];
  if (!row) return '';
  return row[lang] || row.th || '';
}

export function resolveErpRoute(deptId, pageId) {
  const dept = ERP_NAV_STRUCTURE.find((d) => d.id === deptId);
  if (!dept) {
    return { dept: ERP_NAV_STRUCTURE[0].id, page: ERP_NAV_STRUCTURE[0].pages[0] };
  }
  const page = dept.pages.includes(pageId) ? pageId : dept.pages[0];
  return { dept: dept.id, page };
}

export function getErpNav(lang) {
  const code = lang === 'en' || lang === 'ko' ? lang : 'th';
  return ERP_NAV_STRUCTURE.map((dept) => ({
    id: dept.id,
    label: labelFor(code, dept.id),
    pages: dept.pages.map((pageId) => ({
      id: pageId,
      label: labelFor(code, pageId),
    })),
  }));
}

