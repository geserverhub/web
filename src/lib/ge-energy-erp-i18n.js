export function readErpLang() {
  if (typeof window === 'undefined') return 'th';
  const saved = localStorage.getItem(GE_ERP_LANG_KEY);
  return saved === 'en' || saved === 'ko' || saved === 'th' ? saved : 'th';
}

export function writeErpLang(code) {
  if (typeof window === 'undefined') return;
  localStorage.setItem(GE_ERP_LANG_KEY, code);
  document.documentElement.lang = code;
}
