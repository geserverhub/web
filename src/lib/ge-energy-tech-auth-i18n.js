export function readGetAuthLang() {
  if (typeof window === 'undefined') return 'th';
  let saved = localStorage.getItem(GET_AUTH_LANG_KEY);
  if (!saved) {
    const legacy = localStorage.getItem('ge_energy_tech_auth_lang');
    if (legacy && GET_AUTH_LANG_OPTIONS.some((o) => o.code === legacy)) {
      saved = legacy;
      localStorage.setItem(GET_AUTH_LANG_KEY, legacy);
    }
  }
  return GET_AUTH_LANG_OPTIONS.some((o) => o.code === saved) ? saved : 'th';
}

export function writeGetAuthLang(code) {
  if (typeof window !== 'undefined') {
    localStorage.setItem(GET_AUTH_LANG_KEY, code);
  }
}

/** @param {'login'|'register'} page */
export function getAuthCopy(page, lang) {
  const table = page === 'register' ? GET_REGISTER_COPY : GET_LOGIN_COPY;
  return table[lang] || table.en || table.th;
}
